/////////////////////////////////////////////////////////////////////////////////////////
//Accept one Number from user And check whether the number is Even or Odd
////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>
#define TRUE 1
#define FALSE 0
typedef int BOOL;

//////////////////////////////////////////////////////////////////////////////////////////
BOOL CheckEven(int iNo)
{
    if( (iNo % 2) == 0 )
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }
}
////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    BOOL bret = FALSE;

    printf("Enter Number\n");
    scanf("%d",&iValue);

    bret = CheckEven(iValue);

    if(bret == TRUE)
    {
        printf("%d is an EVEN Number\n",iValue);
    }
    else
    {
        printf("%d is an ODD Number\n",iValue);
    }

    return 0;
}
